    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
      
        try {
            if (jtfCNH.getText().equals("")|| jtfNome.getText().equals ("")|| jtfEndereco.getText().equals("") || jtfTelefone.getText().equals ("")) {
                throw new Exception ("Nao deve conter campos nulo");
            }
            int cnh = Integer.parseInt(jtfCNH.getText());
            String nome = jtfNome.getText();
            String endereco = jtfEndereco.getText();
            int telefone = Integer.parseInt(jtfTelefone.getText());
            Motorista m  = new Motorista(cnh, nome, endereco, telefone);
            
            Session session = HibernateUtil.getSession();
            MotoristaDAO dao = new MotoristaDAO(session);
            
            dao.salva(m);
            LimparTela();
            JOptionPane.showMessageDialog(null, "Salvo com sucesso!");
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed
 
